
public class Carro {
	private String cor;
	private String modelo;
	private String marca;
	private String placa;
	
		public void setCor(String cor) {
			this.cor = cor;
		}
		
		public String getCor() {
			return this.cor;
		}
		
		public void setModelo(String cor) {
			this.modelo = cor;
		}
		
		public String getModelo() {
			return this.modelo;
		}
		
		public void setMarca(String cor) {
			this.marca = cor;
		}
		
		public String getMarca() {
			return this.marca;
		}
		
		public void setPlaca(String cor) {
			this.placa = cor;
		}
		
		public String getPlaca() {
			return this.placa;
		}
}
